/**
 * Fitted per-camera PROFILE residual — the Adobe-Color CHROMA character match
 * (docs/brief-bank/profile-fit.md; COLOR.md "Default rendering").
 *
 * A LUMA-NEUTRAL residual 3D transform: it shifts hue/chroma toward Lightroom
 * Classic's Adobe Color rendering (cleaner neutrals, per-hue chroma) while
 * leaving LUMINANCE untouched — luminance is the base curve's / exposure's
 * job. Applied FIRST in the Develop node's fixed order (before the base curve
 * / any user slider, like the camera profile Adobe bakes in), amount 0..100
 * blending identity -> T. Seeded to 100 on fresh RAW opens under the default-
 * look gate, 0 for JPEG / restored docs (appStore.seedDefaultLook).
 *
 * Representation: a 17^3 RESIDUAL lattice of deltas (target - nodePos) in
 * working-linear Rec.2020 over the [0,1]^3 NEUTRAL (pre-base-curve) domain,
 * each delta's LUMA component projected out (T never changes luminance).
 * T(p) = p + residual(p); identity is all-zero, so unseen colors extrapolate
 * to IDENTITY (never a lattice-edge clamp). The trilinear eval (profileResidual)
 * is the EXACT mirror of the WGSL pass (developNode.ts), 1/255 GPU/CPU parity.
 *
 * FIT NOTE (important): fitted 2026-07-12 from 3 LR pairs (DSC02993 / DSC07349 /
 * DSC03298 ARW <-> LR-Classic default exports). Held-out CHROMA dEab mean
 * 7.73 -> 6.21 at amount 100 — a real but MODEST chroma correction. The larger
 * full-dE2000 gain seen while fitting (7.67 -> ~4.4) was DOMINATED by a
 * per-scene LUMINANCE mismatch (the base curve is calibrated on ONE scene,
 * DSC02993; the two Italy scenes render darker than LR) — that is the BASE
 * CURVE's job, and baking it into the profile would brighten well-exposed
 * shots via cross-scene color contamination, so the luma component is
 * deliberately projected out. Recommended follow-up: refit the base curve
 * across multiple scenes, then re-fit this profile for a cleaner chroma
 * character. Refit:  npm run fit:profile [<arw> <lr.jpg> ...]
 */

