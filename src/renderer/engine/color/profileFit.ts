/** Per-camera lookup, keyed by PreparedImage.capture.cameraModel (libraw id). */
export const PROFILE_BY_MODEL: Record<string, readonly number[]> = {
  "ILCE-7CM2": A7C2_PROFILE,
};

/**
 * Fallback for any RAW without a model-specific entry: the a7C II residual is a
 * reasonable Adobe-ish default until each body is measured (fully dial-able via
 * the amount slider / removable at amount 0).
 */
export const DEFAULT_PROFILE: readonly number[] = A7C2_PROFILE;

/** The profile residual for camera model `model` (or the fallback). */
export function profileForModel(model: string | null | undefined): readonly number[] {
  return (model && PROFILE_BY_MODEL[model]) || DEFAULT_PROFILE;
}

const N = PROFILE_LATTICE_N;

/**
 * Trilinear residual at working-linear (r,g,b). Clamps the look-up coord to
 * [0,1] (out-of-gamut inputs read ~identity, never an edge-clamped shift). The
 * EXACT algorithm the WGSL pass mirrors (developNode.ts PROFILE_WGSL) — keep
 * them in lockstep.
 */
export function profileResidual(lat: readonly number[], r: number, g: number, b: number): [number, number, number] {
  const p = [r, g, b];
  const i0 = [0, 0, 0];
  const f = [0, 0, 0];
  for (let k = 0; k < 3; k++) {
    const c = Math.min(1, Math.max(0, p[k]!)) * (N - 1);
    i0[k] = Math.min(N - 2, Math.floor(c));
    f[k] = c - i0[k]!;
  }
  let ro = 0;
  let go = 0;
  let bo = 0;
  for (let dx = 0; dx < 2; dx++)
    for (let dy = 0; dy < 2; dy++)
      for (let dz = 0; dz < 2; dz++) {
        const w = (dx ? f[0]! : 1 - f[0]!) * (dy ? f[1]! : 1 - f[1]!) * (dz ? f[2]! : 1 - f[2]!);
        const base = (((i0[0]! + dx) * N + (i0[1]! + dy)) * N + (i0[2]! + dz)) * 3;
        ro += w * lat[base]!;
        go += w * lat[base + 1]!;
        bo += w * lat[base + 2]!;
      }
  return [ro, go, bo];
}

/** Apply the profile at `amount` (0..100): p + (amount/100)*residual(p). */
export function applyProfileCpu(
  lat: readonly number[],
  px: readonly [number, number, number],
  amount: number
): [number, number, number] {
  const res = profileResidual(lat, px[0], px[1], px[2]);
  const a = amount / 100;
  return [px[0] + a * res[0], px[1] + a * res[1], px[2] + a * res[2]];
}

/**
 * GPU storage-buffer payload: the AMOUNT-SCALED residual, one vec4 per node
 * (xyz = (amount/100)*delta, w unused). Baking amount in keeps the pass to a
 * single storage binding (no separate amount uniform) — repacked per
 * compileDevelop, cheap.
 */
export function packProfileLattice(lat: readonly number[], amount: number): Float32Array {
  const a = amount / 100;
  const out = new Float32Array(N * N * N * 4);
  const nodes = N * N * N;
  for (let n = 0; n < nodes; n++) {
    out[n * 4] = a * lat[n * 3]!;
    out[n * 4 + 1] = a * lat[n * 3 + 1]!;
    out[n * 4 + 2] = a * lat[n * 3 + 2]!;
  }
  return out;
}
